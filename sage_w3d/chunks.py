"""W3D chunk-id registry and the small value types shared across every modeled chunk: the
raw-chunk fallback (`UnknownChunk`), the exception and diagnostic types, and the two structs
(`Rgba`, `Version`) that appear inside many different chunks rather than owning one of their
own.

A `.w3d` file is a flat sequence of chunks: uint32 LE `chunk_type`, uint32 LE `size_field` whose
low 31 bits are the payload length and whose top bit (`flagged`) is conventionally set when the
payload holds nested chunks - conventionally, because real exporters do not always set it
consistently for the same chunk type, so parsing here always dispatches on `chunk_type`, never
on that bit (see `sage_w3d.binary`)."""

