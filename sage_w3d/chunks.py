def chunk_name(chunk_type: int) -> str:
    """The chunk's registry name, or `UNKNOWN_0x<hex>` for an id this package has never seen."""
    name = CHUNK_NAMES.get(chunk_type)
    if name is not None:
        return name
    return f"UNKNOWN_0x{chunk_type:08X}"


class W3DError(Exception):
    """Raised only when the input is not a chunk stream at all: 1 to 7 bytes (too short to hold
    even one chunk header - zero bytes is instead a valid, empty chunk stream), or a first chunk
    header whose declared payload size overruns the file. Anything recoverable - a malformed
    chunk further in, an unmodeled or unrecognized chunk id - is a diagnostic instead, never an
    exception."""


@dataclass
class W3DDiagnostic:
    """One recoverable parsing note: a chunk that didn't match its typed model, was truncated,
    or overran its container, and was kept as raw bytes instead. `chunk_type` is `None` for a
    file-level diagnostic (trailing bytes after the last top-level chunk)."""

    offset: int
    chunk_type: int | None
    message: str


@dataclass
class UnknownChunk:
    """A chunk kept as raw bytes: an id this package has no field model for, or one whose typed
    model didn't reproduce its payload exactly (see `sage_w3d.binary.parse_leaf` and
    `parse_container`). `data` is the exact payload - writing it back reproduces the chunk
    byte-for-byte regardless of why it ended up here."""

    chunk_type: int
    flagged: bool
    data: bytes


@dataclass(frozen=True)
class Version:
    """A chunk version number: on disk a single uint32, `major` in the high 16 bits and `minor`
    in the low 16."""

    major: int
    minor: int

    @staticmethod
    def parse(raw: int) -> "Version":
        return Version(major=raw >> 16, minor=raw & 0xFFFF)

    def encode(self) -> int:
        return ((self.major & 0xFFFF) << 16) | (self.minor & 0xFFFF)


@dataclass
class Rgba:
    """A 4-byte color/alpha quad. Stored as the raw 0-255 ints a chunk holds on disk - never as
    floats - so a value outside the "normal" color range still round-trips exactly."""

    r: int
    g: int
    b: int
    a: int
